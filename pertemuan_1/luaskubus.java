public class luaskubus {
    public static void main(String[] args) {
        float sisi = 5f; // Ganti dengan panjang sisi kubus
        float luasPermukaan = 6 * sisi * sisi; // Menghitung luas permukaan kubus
        System.out.println("Panjang Sisi Kubus: " + sisi); // Menampilkan panjang sisi kubus
        System.out.println("Luas Permukaan Kubus: " + luasPermukaan); // Menampilkan hasil
    }
}
