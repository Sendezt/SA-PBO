public class luassegitiga {
    public static void main(String[] args) {
        float alas = 5f; // Ganti dengan panjang alas segitiga
        float tinggi = 3f; // Ganti dengan tinggi segitiga
        float luas = (alas * tinggi) / 2; // Menghitung luas segitiga
        System.out.println("Panjang Alas Segitiga: " + alas); // Menampilkan panjang alas
        System.out.println("Tinggi Segitiga: " + tinggi); // Menampilkan tinggi segitiga
        System.out.println("Luas Segitiga: " + luas); // Menampilkan hasil
    }
}
