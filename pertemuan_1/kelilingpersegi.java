public class kelilingpersegi {
    public static void main(String[] args) {
        float sisi = 5.4f; // Ganti dengan panjang sisi persegi
        float keliling = 4 * sisi; // Menghitung keliling persegi
        System.out.println("Panjang Sisi Persegi: " + sisi + "cm"); // Menampilkan panjang sisi
        System.out.println("Keliling Persegi: " + keliling + "cm"); // Menampilkan hasil
    }
}
