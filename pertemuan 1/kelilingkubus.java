public class kelilingkubus {
    public static void main(String[] args) {
        float sisi = 5f; // Ganti dengan panjang sisi kubus
        float keliling = 12 * sisi; // Menghitung keliling kubus
        System.out.println("Panjang Sisi Kubus: " + sisi); // Menampilkan panjang sisi kubus
        System.out.println("Keliling Kubus: " + keliling); // Menampilkan hasil
    }
}
